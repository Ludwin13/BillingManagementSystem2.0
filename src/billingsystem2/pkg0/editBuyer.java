    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        String buyer_id = jTextField5.getText();
        
        int a = JOptionPane.showConfirmDialog(null, "Are you sure you want to remove this buyer?","Delete Buyer", JOptionPane.YES_NO_OPTION);
        if (a == 0){
            try {
                Connection conn = mysql_connector.getCon();
                String deleteBuyer = "delete from buyer_tbl where buyer_id = ?";
                PreparedStatement pst = conn.prepareStatement(deleteBuyer);
            
                pst.setString(1, buyer_id);
                pst.executeUpdate();
                    
                setVisible(false);
                JOptionPane.showMessageDialog(null, "Successfully deleted User");
                    
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Failed Connection");
                    e.printStackTrace();
                }
            
            }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel)jTable2.getModel();
        tableModel.setRowCount(0);
        
        
        // Start of try-catch query block
        try {

            // Initialize connection and Statement
            Connection conn = mysql_connector.getCon();
            Statement st = conn.createStatement();
            String sql = "select * from buyer_tbl";

            ResultSet rs = st.executeQuery(sql);

            while(rs.next()) {
                // Retrieves string value of data from the product_tbl in our database
                String id = String.valueOf(rs.getInt("buyer_id"));
                String name = rs.getString("buyer_name");
                String contact = String.valueOf(rs.getInt("buyer_contact"));
                String email = rs.getString("buyer_email");
                String address = rs.getString("buyer_address");
                String gender = rs.getString("buyer_gender");
                
                // Stores the retrieved data from product_tbl to the tblData[] array
                String tblData[] = {id, name, contact, email, address, gender};
                

                // Adds string array data to jTable1

                tableModel.addRow(tblData);
                jTable2.revalidate();

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Failed connection");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

