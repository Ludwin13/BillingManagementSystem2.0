    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        // TextField variables
        String name = jTextField1.getText();
        String contactNo = jTextField2.getText();
        String email = jTextField3.getText();
        String address = jTextField4.getText();
        String buyer_id = jTextField5.getText();
        String gender = (String)jComboBox1.getSelectedItem();

        String updateBuyer = "update buyer_tbl set buyer_name = ?, buyer_contact = ?, buyer_email = ?, buyer_address = ?, buyer_gender = ? where buyer_id = ?";
        String findbuyerID = "select * from buyer_tbl";
        // Insert query using the data retrieved from TextField variables
       try {
           Connection conn = mysql_connector.getCon();
         
           PreparedStatement pst = conn.prepareStatement(updateBuyer);


                    pst.setString(1, name);
                    pst.setString(2, contactNo);
                    pst.setString(3, email);
                    pst.setString(4, address);
                    pst.setString(5, gender);
                    pst.setString(6, buyer_id);


                    pst.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Successfully Updated");
                    setVisible(false);
                    new editBuyer().setVisible(true);
                
                        
       }catch(Exception e){
           JOptionPane.showMessageDialog(this, e);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

